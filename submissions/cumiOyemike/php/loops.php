<?php

// This program displays every number between 100 and 150 each on a new line
for ($i = 101; $i < 150; $i++) {
    echo "$i<br>";
};

// This program displays all even numbers between 0 and 50 each on a new line
for ($i = 1; $i < 50; $i++) {
    if ($i % 2 === 0) {
        echo "$i<br>";
    };
};